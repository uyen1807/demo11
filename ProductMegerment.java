    private void loadData() {
        DataProcess dt = new DataProcess();
        ArrayList<Product> list = dt.getData();
        DefaultTableModel model = (DefaultTableModel)tblContent.getModel();
        model.getDataVector().clear();
        for(Product p:list)
        {
            Vector v = new Vector();
            v.add(p.getId());
            v.add(p.getName());
            v.add(p.getPrice());
            v.add(p.getQuantity());
            model.addRow(v);
        }
        tblContent.setModel(model);
    }
