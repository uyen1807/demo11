    private void btnAddNewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddNewActionPerformed
        // TODO add your handling code here:
        String id = txtId.getText();
        String name = txtName.getText();
        float price = Float.parseFloat(txtPrice.getText());
        int quan = Integer.parseInt(txtQuantity.getText());
        Product p = new Product(id, name, price, quan);
        DataProcess dt = new DataProcess();
        if(dt.addProduct(p))
        {
            JOptionPane.showMessageDialog(this, "AddNew Success", "Result", JOptionPane.PLAIN_MESSAGE);
            loadData();
        }
    }//GEN-LAST:event_btnAddNewActionPerformed

    private void tblContentMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblContentMouseClicked
        int index = tblContent.getSelectedRow();
        if(index >= 0){
            String id = tblContent.getModel().getValueAt(index, 0).toString();
            txtId.setText(id);
            txtName.setText(tblContent.getModel().getValueAt(index, 1).toString());
            txtPrice.setText(tblContent.getModel().getValueAt(index, 2).toString());
            txtQuantity.setText(tblContent.getModel().getValueAt(index, 3).toString());
            setEnableButtons(true);
            setEdittableControls(true);
        }
    }//GEN-LAST:event_tblContentMouseClicked

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        String id = txtId.getText();
        DataProcess dt = new DataProcess();
        if(dt.deleteProduct(id)){
            JOptionPane.showConfirmDialog(this, "Delete success");
            loadData();
            clearDataControls();
            setEnableButtons(false);
        }else{
            JOptionPane.showConfirmDialog(this, "Delete not success");
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        String id = txtId.getText();
        String name = txtName.getText();
        float price = Float.parseFloat(txtPrice.getText());
        int quantity = Integer.parseInt(txtQuantity.getText());
        DataProcess dt = new DataProcess();
        if(dt.updateProduct(id, name, price, quantity)){
            JOptionPane.showConfirmDialog(this, "Update success");
            loadData();
            clearDataControls();
            setEnableButtons(false);
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

